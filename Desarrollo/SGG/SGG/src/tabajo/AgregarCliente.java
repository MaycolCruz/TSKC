    private void InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarActionPerformed
        // TODO add your handling code here:
        float peso = Float.parseFloat(txtpeso.getText());
        float talla = Float.parseFloat(txtTalla.getText());
        float imc=peso/(talla*talla);
        String []info = new String[6];
        info[0]=txtNom.getText();
        info[1]=txtAp.getText();
        info[2]=txtDNI.getText();
        info[3]=""+imc;
        info[4]=txtCel.getText();
        info[5]=txtemail.getText();
        
        modelo.addRow(info);
        
        txtNom.setText("");
        txtAp.setText("");
        txtDNI.setText("");
        txtpeso.setText("");
        txtTalla.setText("");
        txtCel.setText("");
        txtemail.setText("");
        jTFEdad.setText("");
        jDCFecha.setCalendar(null);
           
        JOptionPane.showMessageDialog(this, "Se proceder√° a revisar el registro del cliente.");
    }//GEN-LAST:event_InsertarActionPerformed
        
